const mongoose = require('mongoose');

// User Schema
const UserSchema = new mongoose.Schema({
  name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const User = module.exports = mongoose.model('user', UserSchema);